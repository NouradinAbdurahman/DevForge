// Generates the shell completion scripts under completions/ from the
// real, live command tree (cli/src/core/commandTree.js's getCommandTree,
// the same introspection the TUI's command search already uses) - not
// hand-maintained, so a new command/subcommand can't silently go
// unlisted. Run `node scripts/generate-completions.mjs` after adding or
// renaming a command; CI checks the output matches (see
// .github/workflows/completions.yml), the same generated-and-committed,
// drift-checked pattern as registry/registry.json.
import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createProgram } from "../cli/src/index.js";
import { getCommandTree } from "../cli/src/core/commandTree.js";

const REPO_ROOT = path.resolve(fileURLToPath(import.meta.url), "..", "..");
const OUT_DIR = path.join(REPO_ROOT, "completions");

const program = createProgram();
const tree = getCommandTree(program);

function collectNames(cmd) {
    const names = [cmd.name, ...(cmd.aliases || [])];
    return names;
}

function bashScript(commands) {
    const topNames = commands.flatMap(collectNames).join(" ");
    const perCommandCases = commands
        .map((cmd) => {
            const subNames = (cmd.subcommands || []).flatMap(collectNames).join(" ");
            if (!subNames) return null;
            const caseNames = collectNames(cmd).join("|");
            return `        ${caseNames})\n            COMPREPLY=($(compgen -W "${subNames}" -- "$cur"))\n            return 0\n            ;;`;
        })
        .filter(Boolean)
        .join("\n");

    return `# DevForgeKit bash completion - generated by scripts/generate-completions.mjs, do not hand-edit.
_devforgekit_completions() {
    local cur prev words cword
    _init_completion || return

    if [[ \${cword} -eq 1 ]]; then
        COMPREPLY=($(compgen -W "${topNames}" -- "$cur"))
        return 0
    fi

    case "\${words[1]}" in
${perCommandCases}
    esac
}
complete -F _devforgekit_completions devforgekit
`;
}

function zshScript(commands) {
    const topEntries = commands
        .map((cmd) => `        '${collectNames(cmd).join("|")}:${(cmd.description || "").replace(/'/g, "'\\''")}'`)
        .join("\n");
    const subFunctions = commands
        .filter((cmd) => (cmd.subcommands || []).length > 0)
        .map((cmd) => {
            const subEntries = cmd.subcommands
                .map((sub) => `            '${collectNames(sub).join("|")}:${(sub.description || "").replace(/'/g, "'\\''")}'`)
                .join("\n");
            const caseNames = collectNames(cmd).join("|");
            return `        ${caseNames})\n            _values 'subcommand' \\\n${subEntries}\n            ;;`;
        })
        .join("\n");

    return `#compdef devforgekit
# DevForgeKit zsh completion - generated by scripts/generate-completions.mjs, do not hand-edit.
_devforgekit() {
    local -a commands
    if (( CURRENT == 2 )); then
        _values 'command' \\
${topEntries}
        return
    fi

    case "\${words[2]}" in
${subFunctions}
    esac
}
_devforgekit
`;
}

// Unlike POSIX single quotes (bash/zsh - 100% literal, no escape
// processing at all), fish's single-quoted strings specifically
// recognize \' and \\ as escapes ("a backslash followed by either a
// single quote or another backslash is removed" - fish's own quoting
// docs). Backslashes must be escaped *before* quotes, or a literal
// backslash already present in a description could combine with the
// backslash this function inserts for an adjacent quote and produce an
// unintended escape sequence instead of the two literal characters
// intended (flagged live by CodeQL: "Incomplete string escaping").
function escapeFishSingleQuoted(s) {
    return s.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
}

function fishScript(commands) {
    const lines = [];
    lines.push("# DevForgeKit fish completion - generated by scripts/generate-completions.mjs, do not hand-edit.");
    for (const cmd of commands) {
        for (const name of collectNames(cmd)) {
            const desc = escapeFishSingleQuoted(cmd.description || "");
            lines.push(`complete -c devforgekit -n "__fish_use_subcommand" -a "${name}" -d '${desc}'`);
        }
        for (const sub of cmd.subcommands || []) {
            for (const subName of collectNames(sub)) {
                const desc = escapeFishSingleQuoted(sub.description || "");
                const condition = `__fish_seen_subcommand_from ${collectNames(cmd).join(" ")}`;
                lines.push(`complete -c devforgekit -n "${condition}" -a "${subName}" -d '${desc}'`);
            }
        }
    }
    return lines.join("\n") + "\n";
}

writeFileSync(path.join(OUT_DIR, "devforgekit.bash"), bashScript(tree.commands));
writeFileSync(path.join(OUT_DIR, "devforgekit.zsh"), zshScript(tree.commands));
writeFileSync(path.join(OUT_DIR, "devforgekit.fish"), fishScript(tree.commands));

console.log(`Generated completions for ${tree.commands.length} top-level commands -> ${OUT_DIR}`);
